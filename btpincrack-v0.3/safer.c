void
do_fr(u_char *x, u_char *kp, int round)
{
	u_char  t;

	if(verbose) {
		printf(decimal ? "round %-2d:   " : "round %02x:   ", round);
		hexdump(x, 16);
		printf(decimal ? "Key [%-2d]:   " : "Key [%02x]:   ", (round << 1) - 1);
		hexdump(kp, 16);
		printf(decimal ? "Key [%-2d]:   " : "Key [%02x]:   ", round << 1);
		hexdump(kp + 16, 16);
	}

	x[ 0] = ef[x[ 0] ^ kp[ 0]] + kp[16];
	x[ 1] = lf[x[ 1] + kp[ 1]] ^ kp[17]; 
	x[ 2] = lf[x[ 2] + kp[ 2]] ^ kp[18]; 
	x[ 3] = ef[x[ 3] ^ kp[ 3]] + kp[19];

	x[ 4] = ef[x[ 4] ^ kp[ 4]] + kp[20];
	x[ 5] = lf[x[ 5] + kp[ 5]] ^ kp[21]; 
	x[ 6] = lf[x[ 6] + kp[ 6]] ^ kp[22]; 
	x[ 7] = ef[x[ 7] ^ kp[ 7]] + kp[23];
 
	x[ 8] = ef[x[ 8] ^ kp[ 8]] + kp[24];
	x[ 9] = lf[x[ 9] + kp[ 9]] ^ kp[25]; 
	x[10] = lf[x[10] + kp[10]] ^ kp[26]; 
	x[11] = ef[x[11] ^ kp[11]] + kp[27];

	x[12] = ef[x[12] ^ kp[12]] + kp[28];
	x[13] = lf[x[13] + kp[13]] ^ kp[29]; 
	x[14] = lf[x[14] + kp[14]] ^ kp[30]; 
	x[15] = ef[x[15] ^ kp[15]] + kp[31];

	x[ 1] += x[ 0]; x[ 0] += x[ 1];
	x[ 3] += x[ 2]; x[ 2] += x[ 3];
	x[ 5] += x[ 4]; x[ 4] += x[ 5];
	x[ 7] += x[ 6]; x[ 6] += x[ 7];
	x[ 9] += x[ 8]; x[ 8] += x[ 9];
	x[11] += x[10]; x[10] += x[11];
	x[13] += x[12]; x[12] += x[13];
	x[15] += x[14]; x[14] += x[15];

	x[ 7] += x[ 0]; x[ 0] += x[ 7];
	x[ 1] += x[ 2]; x[ 2] += x[ 1];
	x[ 3] += x[ 4]; x[ 4] += x[ 3];
	x[ 5] += x[ 6]; x[ 6] += x[ 5];
	x[11] += x[ 8]; x[ 8] += x[11];
	x[ 9] += x[10]; x[10] += x[ 9];
	x[15] += x[12]; x[12] += x[15];
	x[13] += x[14]; x[14] += x[13];

	x[ 3] += x[ 0]; x[ 0] += x[ 3];
	x[15] += x[ 2]; x[ 2] += x[15];
	x[ 7] += x[ 4]; x[ 4] += x[ 7];
	x[ 1] += x[ 6]; x[ 6] += x[ 1];
	x[ 5] += x[ 8]; x[ 8] += x[ 5];
	x[13] += x[10]; x[10] += x[13];
	x[11] += x[12]; x[12] += x[11];
	x[ 9] += x[14]; x[14] += x[ 9];

	x[13] += x[ 0]; x[ 0] += x[13];
	x[ 5] += x[ 2]; x[ 2] += x[ 5];
	x[ 9] += x[ 4]; x[ 4] += x[ 9];
	x[11] += x[ 6]; x[ 6] += x[11];
	x[15] += x[ 8]; x[ 8] += x[15];
	x[ 1] += x[10]; x[10] += x[ 1];
	x[ 3] += x[12]; x[12] += x[ 3];
	x[ 7] += x[14]; x[14] += x[ 7];

	t = x[0]; x[0] = x[14]; x[14] = x[12]; x[12] = x[10]; x[10] = x[2]; 
	x[2] = x[8]; x[8] = x[4]; x[4] = t;

	t = x[1]; x[1] = x[7]; x[7] = x[11]; x[11] = x[5]; x[5] = x[13]; x[13] = t; 
	
	t = x[15]; x[15] = x[3]; x[3] = t;
};

void
safer_set_key(u_char *lk_)
{
	u_char  by, lk[17];
	u_long  i, j, k, l, m;

	memcpy(lk, lk_, 16);
	lk[k_bytes] = 0;

	for(i = 0; i < k_bytes; i++) {
		lk[k_bytes] ^= lk[i];
		l_key[i] = lk[i];
	}

	for(i = 0; i < k_bytes; i++) {
		for(j = 0; j <= k_bytes; ++j) {
			by = lk[j];
			lk[j] = by << 3 | by >> 5;
		}

		k = (17 * i) + 35; l = (i << 4) + 16; m = i + 1;

		for(j = 0; j < 16; ++j) {
			l_key[l + j] = lk[m] + ef[ef[(k + j) & 255]];
			m = (m == k_bytes ? 0 : m + 1);
		}
	}
};

void
safer(u_char *blk, int modified)
{
	u_char *kp;
	u_char blk0[16];

	memcpy(blk0, blk, 16);
	do_fr(blk, l_key, 1);	 do_fr(blk, l_key +  32, 2); 
	if(modified) {
/*
		printf("blk0: ");
		hexdump(blk0, 16);
		printf("blk: ");
		hexdump(blk, 16);
*/
		blk[ 0] ^= blk0[ 0];
		blk[ 1] += blk0[ 1]; blk[ 2] += blk0[ 2];
		blk[ 3] ^= blk0[ 3]; blk[ 4] ^= blk0[ 4];
		blk[ 5] += blk0[ 5]; blk[ 6] += blk0[ 6];
		blk[ 7] ^= blk0[ 7]; blk[ 8] ^= blk0[ 8];
		blk[ 9] += blk0[ 9]; blk[10] += blk0[10];
		blk[11] ^= blk0[11]; blk[12] ^= blk0[12];
		blk[13] += blk0[13]; blk[14] += blk0[14];
		blk[15] ^= blk0[15];
/*
		printf("blk: ");
		hexdump(blk, 16);
*/
	}
	do_fr(blk, l_key +  64, 3); do_fr(blk, l_key +  96, 4);
	do_fr(blk, l_key + 128, 5); do_fr(blk, l_key + 160, 6);
	do_fr(blk, l_key + 192, 7); do_fr(blk, l_key + 224, 8);
	
	kp = l_key + (k_bytes * 16);

	blk[ 0] ^= kp[ 0]; blk[ 1] += kp[ 1];
	blk[ 2] += kp[ 2]; blk[ 3] ^= kp[ 3]; 
	blk[ 4] ^= kp[ 4]; blk[ 5] += kp[ 5];
	blk[ 6] += kp[ 6]; blk[ 7] ^= kp[ 7]; 
	blk[ 8] ^= kp[ 8]; blk[ 9] += kp[ 9];
	blk[10] += kp[10]; blk[11] ^= kp[11]; 
	blk[12] ^= kp[12]; blk[13] += kp[13];
	blk[14] += kp[14]; blk[15] ^= kp[15]; 

	if(verbose) {
		printf(decimal ? "Key [%-2d]:   " : "Key [%02x]:   ", 17);
		hexdump(kp, 16);
	}
};

u_char *
Ar(u_char *lk, u_char *blk)
{
	static u_char out[16];
/*
	printf("lk:  "); hexdump(lk, 16);
	printf("blk: "); hexdump(blk, 16);
*/
	safer_set_key(lk);
	memcpy(out, blk, 16);
	safer(out, 0);
	return out;
}

u_char *
Ar_(u_char *lk, u_char *blk)
{
	static u_char out[16];
/*
	printf("lk:  "); hexdump(lk, 16);
	printf("blk: "); hexdump(blk, 16);
*/
	safer_set_key(lk);
	memcpy(out, blk, 16);
	safer(out, 1);
	return out;
}
